=begin
Em Ruby, há vários operadores aritméticos que 
permitem que você execute cálculos matemáticos. 
Abaixo estão os operadores aritméticos em Ruby:

Adição: o operador "+" é usado para adicionar dois valores juntos. 
Por exemplo:

a = 5
b = 3
c = a + b
puts c  # output: 8


Subtração: o operador "-" é usado 
para subtrair um valor de outro. Por exemplo:
a = 5
b = 3
c = a - b
puts c  # output: 2


Multiplicação: o operador "*" é usado para 
multiplicar dois valores. Por exemplo:
a = 5
b = 3
c = a * b
puts c  # output: 15


Divisão: o operador "/" é usado para 
dividir um valor pelo outro. Por exemplo:
a = 10
b = 2
c = a / b
puts c  # output: 5


Módulo: o operador "%" é usado para encontrar o 
resto da divisão de um valor pelo outro. Por exemplo:
a = 10
b = 3
c = a % b
puts c  # output: 1


Potência: o operador "**" é usado para elevar 
um valor a uma potência. Por exemplo:
a = 2
b = 3
c = a ** b
puts c  # output: 8
=end

