
#Integer: representa um número inteiro positivo ou negativo.
a = 10 #decimal
b = 0xa #hexadecimal
c = 012 #octal
d = 0b1010 #binário

#Float: representa um número decimal com ponto flutuante.
ab = 3.14
bc = -0.123

#booleanos(Boolean): representa um valor lógico verdadeiro(true) ou falso(false).
abc = true
bcd = false

#Strings(String): Representa uma sequência de caracteres.
phrase = "Hello, World!"
word = "Ruby"

=begin
Além dos tipos primitivos, Ruby permite a definição de 
variáveis para armazenar valores. 
As variáveis podem ser definidas com um nome e um valor inicial.  

    a = 10
    b = "Hello, world!"
    c = true

Os nomes das variáveis em Ruby seguem algumas regras, tais como:

O nome deve começar com uma letra minúscula ou um sublinhado (_).
O nome pode conter letras, números e sublinhados.
O nome é sensível a maiúsculas e minúsculas.
=end