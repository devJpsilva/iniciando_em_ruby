Em Ruby, os operadores relacionais são usados para 
comparar valores e retornar um resultado booleano 
(verdadeiro ou falso) com base na relação entre eles. 
Os operadores relacionais disponíveis em Ruby são:

==: Retorna verdadeiro se os valores comparados são iguais.
!=: Retorna verdadeiro se os valores comparados são diferentes.
>: Retorna verdadeiro se o valor da esquerda é maior que o valor da direita.
<: Retorna verdadeiro se o valor da esquerda é menor que o valor da direita.
>=: Retorna verdadeiro se o valor da esquerda é maior ou igual ao valor da direita.
<=: Retorna verdadeiro se o valor da esquerda é menor ou igual ao valor da direita.

Aqui está um exemplo de como usar os operadores relacionais em Ruby:
=end
a = 10
b = 5

puts a == b  # false
puts a != b  # true
puts a > b   # true
puts a < b   # false
puts a >= b  # true
puts a <= b  # false